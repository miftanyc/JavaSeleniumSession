// Relational Operators

package CoreJava.operators;

public class RelationalOperators {
    public static void main(String[] args) {
        //Relational Operators

        int a = 5, b = 10, c = 5, d = 9;

        System.out.println(a = b); // a=b means values of b is now values of a means 10
        System.out.println(a == d); // a==d equal to operators mean is a is equal to b if not its false
        System.out.println(a != d);
        System.out.println(a > c);
        System.out.println(a < c);
        System.out.println(a >= c);
        System.out.println(a <= c);
    }
}
