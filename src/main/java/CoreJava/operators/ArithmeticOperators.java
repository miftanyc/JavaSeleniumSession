package CoreJava.operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        //Arithmetic Operators

        int a = 5, b = 10, c = 14;

        System.out.println("addition "+ (a + b));
        System.out.println("Suibstruction "+(a - b));
        System.out.println("Multiplication "+(a * b));
        System.out.println("Divistion " +(b / a)); // Division result 2
        System.out.println("Reminder 14/5: "+(c % a)); // Reminder of division which is 4
        System.out.println("Increase the value by 1: "+ (++a)); // Increase the value of a by 1
        System.out.println("Decreaes the value by 1: "+ (--b)); // Decrease the value of b by 1

    }
}
